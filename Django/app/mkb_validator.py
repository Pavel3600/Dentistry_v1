# Обратный индекс для поиска по названию
NAME_TO_CODE: Dict[str, str] = {}
for code, name in MKB_DENTISTRY_CODES.items():
    NAME_TO_CODE[name.lower()] = code
    NAME_TO_CODE[code.lower()] = code


def validate_mkb_code(code: str) -> Tuple[bool, Optional[str]]:
    """
    Проверяет, существует ли код МКБ-С-3 в базе.

    Args:
        code: Код МКБ-С-3 (например, "K02.0")

    Returns:
        Tuple[bool, Optional[str]]: (существует, название диагноза)
    """
    if not code:
        return False, None

    # Нормализация кода (удаление пробелов, приведение к верхнему регистру)
    normalized = code.strip().upper()

    if normalized in MKB_DENTISTRY_CODES:
        return True, MKB_DENTISTRY_CODES[normalized]

    return False, None


def get_mkb_name(code: str) -> str:
    """
    Возвращает название диагноза по коду МКБ-С-3.

    Args:
        code: Код МКБ-С-3

    Returns:
        str: Название диагноза или сообщение об ошибке
    """
    exists, name = validate_mkb_code(code)
    if exists:
        return name
    return f"Код {code} не найден в классификаторе МКБ-С-3"


def search_mkb_by_name(query: str) -> List[Tuple[str, str]]:
    """
    Поиск кодов МКБ-С-3 по названию диагноза.

    Args:
        query: Поисковый запрос

    Returns:
        List[Tuple[str, str]]: Список (код, название)
    """
    if not query:
        return []

    query_lower = query.lower()
    results = []

    for code, name in MKB_DENTISTRY_CODES.items():
        if query_lower in name.lower() or query_lower in code.lower():
            results.append((code, name))

    return results[:20]  # Ограничиваем 20 результатами


def get_mkb_code_by_name(name: str) -> Optional[str]:
    """
    Получает код МКБ-С-3 по названию диагноза.

    Args:
        name: Название диагноза

    Returns:
        Optional[str]: Код диагноза или None
    """
    if not name:
        return None

    name_lower = name.strip().lower()
    return NAME_TO_CODE.get(name_lower)


def get_all_mkb_codes() -> List[Tuple[str, str]]:
    """
    Возвращает все коды МКБ-С-3.

    Returns:
        List[Tuple[str, str]]: Список всех (код, название)
    """
    return sorted(MKB_DENTISTRY_CODES.items())


def get_mkb_by_category(category: str) -> List[Tuple[str, str]]:
    """
    Возвращает коды МКБ-С-3 по категории (первые 3 символа кода).

    Args:
        category: Категория (например, "K02" для кариеса)

    Returns:
        List[Tuple[str, str]]: Список кодов в категории
    """
    if not category:
        return []

    category_upper = category.strip().upper()
    results = []

    for code, name in MKB_DENTISTRY_CODES.items():
        if code.startswith(category_upper):
            results.append((code, name))

    return results


def format_mkb_for_print(code: str) -> str:
    """
    Форматирует код МКБ-С-3 для печати.

    Args:
        code: Код МКБ-С-3

    Returns:
        str: Отформатированная строка "Код – Название"
    """
    exists, name = validate_mkb_code(code)
    if exists:
        return f"{code} – {name}"
    return f"{code} – ???"


def get_mkb_statistics() -> Dict[str, int]:
    """
    Возвращает статистику по категориям МКБ-С-3.

    Returns:
        Dict[str, int]: Количество кодов по категориям
    """
    stats = {}
    for code in MKB_DENTISTRY_CODES.keys():
        category = code.split('.')[0] if '.' in code else code[:3]
        stats[category] = stats.get(category, 0) + 1
    return dict(sorted(stats.items()))


