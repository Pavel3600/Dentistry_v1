    def test_dentist_can_open_report_form(self, client, user_dentist, visit):
        """Стоматолог открывает форму отчёта о визите — статус 200."""
        # Arrange
        client.force_login(user_dentist)

        # Act
        r = client.get(reverse('app:visit_report', args=[visit.pk]))

        # Assert
        assert r.status_code == 200

    def test_dentist_can_write_report(self, client, user_dentist, visit):
        """Стоматолог успешно сохраняет отчёт о визите — объект VisitReport создаётся в БД."""
        # Arrange
        client.force_login(user_dentist)

        # Act
        r = client.post(reverse('app:visit_report', args=[visit.pk]), {
            'title': 'Отчёт о приёме',
            'summary': 'Проведено лечение кариеса.',
            'recommendations': 'Контроль через 6 мес.',
            'complications': '',
        })

        # Assert
        assert r.status_code == 302
        report = VisitReport.objects.get(visit=visit)
        assert report.summary.startswith('Проведено')
        assert report.author == user_dentist

    def test_report_requires_summary(self, client, user_dentist, visit):
        """Отчёт без поля summary не сохраняется — форма возвращается с ошибкой (200)."""
        # Arrange
        client.force_login(user_dentist)

        # Act
        r = client.post(reverse('app:visit_report', args=[visit.pk]), {
            'title': 'Без заключения', 'summary': '',
        })

        # Assert
        assert r.status_code == 200  # форма с ошибкой
        assert not VisitReport.objects.filter(visit=visit).exists()

    def test_str(self, visit, user_dentist):
        """Строковое представление VisitReport содержит слово «визит»."""
        # Arrange
        report = VisitReport.objects.create(
            visit=visit, author=user_dentist, summary='x',
        )

        # Act / Assert
        assert 'визит' in str(report).lower()

    def test_patient_cannot_write_report(self, client, user_patient, visit):
        """Пациент не имеет доступа к форме отчёта о визите — статус 302 или 403."""
        # Arrange
        client.force_login(user_patient)

        # Act
        r = client.get(reverse('app:visit_report', args=[visit.pk]))

        # Assert
        assert r.status_code in (302, 403)
