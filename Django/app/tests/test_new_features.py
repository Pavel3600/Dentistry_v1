class TestPatientCardNumber:
    def test_card_number_autogenerated_on_save(self):
        """При создании пациента без явного card_number он генерируется автоматически с префиксом K-."""
        # Arrange
        # Act
        p = Patient.objects.create(
            full_name='Безномерный Пациент',
            birth_date='1990-01-01 00:00:00',
            gender='M', phone='+79990000001',
        )

        # Assert
        assert p.card_number
        assert p.card_number.startswith('K-')

    def test_card_numbers_are_unique_sequential(self):
        """Два разных пациента получают уникальные номера карт."""
        # Arrange / Act
        p1 = Patient.objects.create(
            full_name='Первый', birth_date='1990-01-01 00:00:00',
            gender='M', phone='+79990000002',
        )
        p2 = Patient.objects.create(
            full_name='Второй', birth_date='1990-01-01 00:00:00',
            gender='M', phone='+79990000003',
        )

        # Assert
        assert p1.card_number != p2.card_number

    def test_explicit_card_number_preserved(self):
        """Если card_number задан явно, он не перезаписывается при сохранении."""
        # Arrange / Act
        p = Patient.objects.create(
            card_number='K-2000-99999',
            full_name='Свой Номер', birth_date='1990-01-01 00:00:00',
            gender='F', phone='+79990000004',
        )

        # Assert
        assert p.card_number == 'K-2000-99999'


class TestPatientCreateView:
    def test_manager_creates_patient_fills_card_and_user(self, client, user_manager):
        """Менеджер создаёт пациента через форму — у пациента заполняются card_number и user_id."""
        # Arrange
        client.force_login(user_manager)

        # Act
