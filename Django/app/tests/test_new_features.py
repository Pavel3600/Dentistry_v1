class TestPatientMedicalInfo:
    def test_has_alerts_property(self, patient):
        """Свойство has_alerts возвращает True, если у пациента есть аллергии."""
        # Arrange
        info = PatientMedicalInfo.objects.create(patient=patient, allergies='Лидокаин')

        # Act / Assert
        assert info.has_alerts is True

    def test_no_alerts_when_empty(self, patient):
        """Свойство has_alerts возвращает False, если поле allergies пустое."""
        # Arrange
        info = PatientMedicalInfo.objects.create(patient=patient, blood_type='A+')

        # Act / Assert
        assert info.has_alerts is False

    def test_dentist_can_open_medical_info(self, client, user_dentist, patient):
        """Стоматолог открывает страницу медицинской информации пациента — статус 200."""
        # Arrange
        client.force_login(user_dentist)

        # Act
        r = client.get(reverse('app:patient_medical_info', args=[patient.pk]))

        # Assert
        assert r.status_code == 200

    def test_dentist_can_save_medical_info(self, client, user_dentist, patient):
        """Стоматолог сохраняет медицинскую информацию пациента — данные записываются в БД."""
        # Arrange
        client.force_login(user_dentist)

        # Act
        r = client.post(reverse('app:patient_medical_info', args=[patient.pk]), {
            'allergies': 'Пенициллин',
            'chronic_conditions': 'Диабет',
            'contraindications': '',
            'blood_type': 'O+',
            'notes': '',
        })

        # Assert
        assert r.status_code == 302
        info = PatientMedicalInfo.objects.get(patient=patient)
        assert info.allergies == 'Пенициллин'
        assert info.blood_type == 'O+'

    def test_update_existing_medical_info(self, client, user_dentist, patient):
        """Повторное сохранение медицинской информации обновляет существующую запись (не создаёт дубликат)."""
        # Arrange
        PatientMedicalInfo.objects.create(patient=patient, allergies='Старое')
        client.force_login(user_dentist)

        # Act
        r = client.post(reverse('app:patient_medical_info', args=[patient.pk]), {
            'allergies': 'Новое', 'chronic_conditions': '', 'contraindications': '',
            'blood_type': '', 'notes': '',
        })

        # Assert
        assert r.status_code == 302
        assert PatientMedicalInfo.objects.filter(patient=patient).count() == 1
        assert PatientMedicalInfo.objects.get(patient=patient).allergies == 'Новое'

    def test_history_shows_allergy_alert(self, client, user_dentist, patient):
        """Страница истории пациента отображает его аллергии из медицинской информации."""
        # Arrange
        PatientMedicalInfo.objects.create(patient=patient, allergies='Латекс')
        client.force_login(user_dentist)

        # Act
        r = client.get(reverse('app:patient_history', args=[patient.pk]))

        # Assert
        assert r.status_code == 200
        assert 'Латекс' in r.content.decode()

    def test_manager_cannot_edit_medical_info(self, client, user_manager, patient):
        """Менеджер не имеет доступа к редактированию медицинской информации — статус 302 или 403."""
        # Arrange
        client.force_login(user_manager)

        # Act
        r = client.get(reverse('app:patient_medical_info', args=[patient.pk]))

        # Assert
        assert r.status_code in (302, 403)


